import os
from data_loader import load_graphml_to_rx, write_rx_to_graphml
from strategies.topological import AverageNeighborStrategy
from strategies.textual import ConcatenatedAbstractsStrategy
from evaluator import evaluate_link_prediction, evaluate_semantic_consistency

def run_experiments(graphml_path: str):
    print(f"Loading graph from {graphml_path}...")
    graph = load_graphml_to_rx(graphml_path)
    
    print(f"Graph loaded with {graph.num_nodes()} nodes and {graph.num_edges()} edges.")
    
    # 1. Topological Strategy
    print("\n--- Running Average Neighbor Strategy ---")
    topo_strategy = AverageNeighborStrategy()
    topo_embeddings = topo_strategy.compute_embeddings(graph, target_entity_type="author")
    print(f"Computed {len(topo_embeddings)} embeddings for authors.")
    
    if topo_embeddings:
        link_pred_score = evaluate_link_prediction(graph, topo_embeddings)
        print(f"Link Prediction (Hit@10): {link_pred_score:.4f}")
        
        sem_consist_score = evaluate_semantic_consistency(graph, topo_embeddings)
        print(f"Semantic Consistency Margin: {sem_consist_score:.4f}")
        
    # 2. Textual Strategy
    print("\n--- Running Concatenated Abstracts Strategy ---")
    textual_strategy = ConcatenatedAbstractsStrategy(max_works=5)
    textual_embeddings = textual_strategy.compute_embeddings(graph, target_entity_type="author")
    print(f"Computed {len(textual_embeddings)} embeddings for authors.")
    
    if textual_embeddings:
        link_pred_score = evaluate_link_prediction(graph, textual_embeddings)
        print(f"Link Prediction (Hit@10): {link_pred_score:.4f}")
        
        sem_consist_score = evaluate_semantic_consistency(graph, textual_embeddings)
        print(f"Semantic Consistency Margin: {sem_consist_score:.4f}")
        
    # Example: Inject best embeddings back into graph and save
    print("\nInjecting topological embeddings back into graph for visualization...")
    for node_idx in graph.node_indices():
        attrs = graph.get_node_data(node_idx)
        if attrs and attrs.get("entity_type") == "author" and attrs.get("id") in topo_embeddings:
            attrs["embedding"] = topo_embeddings[attrs["id"]]
            graph[node_idx] = attrs
            
    out_path = graphml_path.replace(".graphml", "_with_author_embeddings.graphml")
    write_rx_to_graphml(graph, out_path)
    print(f"Saved enriched graph to {out_path}. You can visualize it with PyAlex atlas.")

if __name__ == "__main__":
    # Point this to a valid graphml file generated by pyalex network build
    sample_graph = "../../test_network.graphml" 
    if os.path.exists(sample_graph):
        run_experiments(sample_graph)
    else:
        print(f"Please provide a valid graphml file at {sample_graph} or run via python and import this module.")
